# Prompt the user to input the first mark
mark1 = float(input("Enter your first school mark: "))

# Prompt the user to input the second mark
mark2 = float(input("Enter your second school mark: "))

# Calculate the arithmetic mean
mean = (mark1 + mark2) / 2

# Print the result
print(f"The arithmetic mean of the marks {mark1} and {mark2} is {mean}")
