# სხვადასხვა მონაცემთა ტიპის ცვლადები
integer_var = 10  # მთელი რიცხვი
float_var = 10.5  # მცურავი წერტილით რიცხვი
string_var = "Hello, World!"  # სტრიქონი
list_var = [1, 2, 3, 4, 5]  # სია
tuple_var = (1, 2, 3)  # ტაპლი
dict_var = {"key1": "value1", "key2": "value2"}  # ლექსიკონი
set_var = {1, 2, 3, 4, 5}  # ნაკრები
bool_var = True  # ბულეანური მნიშვნელობა
none_var = None  # None ტიპი

# ტიპების შემოწმება და ჩვენება
print(f"{integer_var} არის {type(integer_var)}")
print(f"{float_var} არის {type(float_var)}")
print(f"{string_var} არის {type(string_var)}")
print(f"{list_var} არის {type(list_var)}")
print(f"{tuple_var} არის {type(tuple_var)}")
print(f"{dict_var} არის {type(dict_var)}")
print(f"{set_var} არის {type(set_var)}")
print(f"{bool_var} არის {type(bool_var)}")
print(f"{none_var} არის {type(none_var)}")
 

