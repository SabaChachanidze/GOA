// let - ცვლადი, რომლის მნიშვნელობა შეიძლება შეიცვალოს
let age = 25;  // ცვლადი age იწერება 25
age = 30;      // მნიშვნელობა შეიცვალა
console.log(age);  // 30

// const - ცვლადი, რომლის მნიშვნელობა არ შეიძლება შეიცვალოს
const birthYear = 1998;  // ცვლადი birthYear ინარჩუნებს მას 1998
// birthYear = 2000;  // შეცვლა შეუძლებელია, რადგან const-ია
console.log(birthYear);  // 1998

// var - ძველი გზა ცვლადების გამოცხადებისთვის, მოქმედებს მთელი ფუნქციის ფარგლებში (function scope), 
// მიუხედავად იმისა, სად არის გამოცხადებული
var name = "John";  // ცვლადი name
name = "Jane";  // მნიშვნელობა შეიცვალა
console.log(name);  // Jane

// let და const გამოიყენება უფრო ხშირად თანამედროვე JavaScript-ში.
// var არ უნდა იქნას გამოყენებული ახალ კოდში.
